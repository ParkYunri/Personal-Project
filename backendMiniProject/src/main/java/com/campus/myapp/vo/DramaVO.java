package com.campus.myapp.vo;

public class DramaVO {
	private int dno;
	private String dsubject;
	private String dcontent;
	private String dposter;
	private String reg;
	
	public int getDno() {
		return dno;
	}
	public void setDno(int dno) {
		this.dno = dno;
	}
	public String getDsubject() {
		return dsubject;
	}
	public void setDsubject(String dsubject) {
		this.dsubject = dsubject;
	}
	public String getDcontent() {
		return dcontent;
	}
	public void setDcontent(String dcontent) {
		this.dcontent = dcontent;
	}
	public String getDposter() {
		return dposter;
	}
	public void setDposter(String dposter) {
		this.dposter = dposter;
	}
	public String getReg() {
		return reg;
	}
	public void setReg(String reg) {
		this.reg = reg;
	}
}
